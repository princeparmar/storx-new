func TestMigrateGob_Redis(t *testing.T) {
	ctx := testcontext.New(t)

	server, err := testredis.Start(ctx)
	require.NoError(t, err)
	defer ctx.Check(server.Close)

	db, err := OpenDB(ctx, "redis://"+server.Addr()+"?db=1", true)
	require.NoError(t, err)
	defer ctx.Check(db.Close)

	isGob, err := isLikelyGobEncoded(expectedGroupDataGob)
	require.NoError(t, err)
	require.True(t, isGob)

	require.NoError(t, db.db.Put(ctx, kvstore.Key("gob"), expectedGroupDataGob))
	require.NoError(t, db.db.Put(ctx, kvstore.Key("pb"), expectedGroupDataProto))

	count, err := db.MigrateGob(ctx, nil)
	require.NoError(t, err)
	require.Equal(t, 1, count)

	data, err := db.db.Get(ctx, kvstore.Key("gob"))
	require.NoError(t, err)
	require.Equal(t, expectedGroupDataProto, []byte(data))

	isGob, err = isLikelyGobEncoded(data)
	require.NoError(t, err)
	require.False(t, isGob)

	data, err = db.db.Get(ctx, kvstore.Key("pb"))
	require.NoError(t, err)
	require.Equal(t, expectedGroupDataProto, []byte(data))
}
